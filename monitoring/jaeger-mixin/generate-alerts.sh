# Generates prometheus_alerts.yml from Pyrra SLO definitions.
# Usage: ./generate-alerts.sh

set -euo pipefail
shopt -s nullglob

PYRRA_VERSION=v0.10.0
SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
TMP_DIR=$(mktemp -d)
trap 'rm -rf "${TMP_DIR}"' EXIT

docker run --rm -u "$(id -u):$(id -g)" \
  -v "${SCRIPT_DIR}/pyrra:/work/pyrra:ro" \
  -v "${TMP_DIR}:/work/out" \
  "ghcr.io/pyrra-dev/pyrra:${PYRRA_VERSION}" generate \
  --config-files "/work/pyrra/*.yaml" \
  --prometheus-folder /work/out/

# Write header and merge all generated rule files into a single valid
# YAML document with one top-level "groups:" key.
{
  echo '# Copyright (c) 2026 The Jaeger Authors.'
  echo '# SPDX-License-Identifier: Apache-2.0'
  echo '#'
  echo '# Code generated by Pyrra. DO NOT EDIT.'
  echo '# To regenerate, run: `make generate-alerts` in the repo root.'
  echo ''
  echo 'groups:'
  for f in "${TMP_DIR}"/*.yaml; do
    # Strip the "groups:" line from each file, keep the list items.
    sed '/^groups:/d' "$f"
  done
} > "${SCRIPT_DIR}/prometheus_alerts.yml"
